a = int(input("Введите номер места: "))
# от 1 до 20 - купе, от 21 до 50 плацкарт
# чет -нижние, нечет - верхние

if 1 <= a <= 20:

    if a % 2 == 0:
        print("Купе, нижнее.")
    else:
        print("Купе, верхнее.")
elif 21 <= a <= 50:
    if a % 2 == 0:
        print("Плацкарт, нижнее.")
    else:
        print("Плацкарт, верхнее.")
else:
        print("Неправильный номер места.")
